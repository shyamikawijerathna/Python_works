#Arithmatic operators
#Addition +
print(5.3+5)

#Multiply
print(2*2)
print(2.*2)
print(2*2.) #If we enter a number with decimal point, result comes with decimal-Float (case sensitive)

#substraction
print(4-2)

#Division
print(2/2)
print(2./2)
print(2/2.)
print(2./2.)

#floor division
print(2//2)  #result 1
print(2.//2) #result 1.0

print(5//3) #result 1
print(5/3) #result 1.6666667

#power
print(5 ** 5) #result 3125
print(5. ** 5) #result 3125.0

#Modules-Remain value
print(5 % 3) #result 2
print(5. % 3) #result 2.0
print(5. % 3.) # result 2.0

print(2+6/3*3) #Result 8.0

print(2 ** 2 ** 3) #calculations are starting from right - Result 256
